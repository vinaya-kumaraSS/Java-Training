public class Program3 {

	public static void main(String[] args) {
		int a = 10;
		int b = 10;
		System.out.println("post increment :"+(a++));
		System.out.println("pre increment :"+(++b));
//		a = 10;
//		b = 10;
		System.out.println("post decrement :"+(a--));
		System.out.println("pre decrement :"+(--b));
	}

}
